package gg.essential.mixins.transformers.server.integrated;

import gg.essential.Essential;
import gg.essential.event.network.server.ServerTickEvent;
import net.minecraft.server.integrated.IntegratedServer;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

@Mixin(IntegratedServer.class)
public abstract class Mixin_IntegratedServerOnTick {
    @Inject(method = "tick", at = @At("HEAD"))
    protected void tick(CallbackInfo ci) {
        Essential.EVENT_BUS.post(new ServerTickEvent());
    }
}
